	//////////////////////////////////////////////////////////////////////////
	//gif文件中一帧的信息
	//////////////////////////////////////////////////////////////////////////
	typedef struct GIFFrame    
	{
		UINT		nSize;
		IPicture*	pPicture;		// 绘制接口(COM)
		SIZE		frameSize;		// 此帧尺寸
		SIZE		frameOffset;	// 此帧位移
		UINT		nDelay;			// 此帧的时延 (in 1/100s of a second)
		UINT		nDisposal;		// 处理方式
	} GIFFRAME, *LPGIFFRAME;


	//////////////////////////////////////////////////////////////////////////
	// image type
	//////////////////////////////////////////////////////////////////////////
	typedef enum enmImageType
	{
		EIT_BMP = 0,
		EIT_TGA = 1	
	} ENMIMAGETYPE;

